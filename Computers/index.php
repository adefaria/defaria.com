  <h2>Career (brief history)</h2>

  <p>I have been involved with computers for as long as I can
  remember.  Way back in high school, when computers were just
  starting to be widely used in business, my interests in math lead me
  to explore the field of computes. Starting in a class using punched
  cards and paper tape I was lucky that my high school was one of the
  few schools that had a class in data processing and access to real
  computers, though they were miles away we had access via a teletype.</p>

  <p>I went to college to be a "computer scientist" but is was really
  just business programming. Still I learned standard COBOL, FORTRAN
  and Assembler on an old, card munching IBM 360 mainframe. From there
  I got a job as a computer operator for a small market research firm
  on an HP/3000 machine. Even though I was an operator I used my
  programming skills to write a few applications in FORTRAN and
  learned SPL (System Programming Language) on my own. I did not know
  it but my involvement in HP equipment and HP itself would continue
  to this day.</p>

  <p>From there I moved to various business programming positions and
  started consulting, writing COBOL, View (screen forms package) and
  Image (database) applications for business on HP systems for some 8
  years.</p>

  <p>In 1987 I moved to California when I got a contract at HP
  Cupertino itself performing testing. After a while I joined HP as a
  permenant employee and was soon reorganized into the Language Labs
  at HP. I was trained and took to HP-UX and within a short time I
  became the Clearcase and Unix administrator for the entire lab of
  some 80-120 engineers. I left HP in 1998 after 10 years.</p>

  <p>Since leaving HP I had contracts as such noname companies in
  Silicon Valley like Sun and Cisco and then back into HP itself.</p>

  <p>Then, as everybody knows, the Internet bubble burst and I eventually 
  hooked up with a start up company, Salira Optical Network System, where I
  am today</p>

  <p>Next on tap, with Salira on the verge of bankruptcy I took a
  contract down in Orange County at Ameriquest Mortgage. LA was nice
  but my family is up here. After a while at Ameriquest I get a job at
  LynuxWorks in San Jose. Ah, working on Linux was nice. But soon it
  was back to contracting for me, now at Broadcom.</p>

  <p>Please refer to my <a href="/resume">resume</a> for a detailed
  listing of my work history.</p>

  <h2><a name="gear"></a>Gear</h2>

  <p>My site is hosted at my home through an always on
  DSL connection. It used to run on my <a
  href="http://microsoft.com/windowsxp">Windows XP</a> box, a 5 year
  old 450 Mhz machine that's still kicking and provided a lot of
  service. Being brought up on <span class="standout">Unix</span> I am
  more comfortable at programming in a <i>Unix mode</i>. As such I
  have discovered and utilize <a href="http://cygwin.com">Cygwin</a>
  which is essentially <span class="standout">Linux</span> running
  under Windows. With it I was able to run my web server (<a
  href="http://apache.org">Apache</a> running under <a
  href="http://cygwin.com">Cygwin</a>), mail server (<a
  href="http://exim.org">Exim</a>), <a
  href="http://www.proftpd.org">FTP</a> server, provide <a
  href="http://www.openssh.com">SSH</a> services as well as a very
  functional development environment using <a
  href="http://www.gnu.org/software/bash/bash.html">Bash</a>, <a
  href="http://perl.com">Perl</a>, <a
  href="http://xemacs.org">XEmacs</a>, <a
  href="http://www.vim.org">vi</a>, etc. That, plus running my XP
  Desktop, browsing the web (I use <a href="http://netscape.com">Netscape</a>
  and now <a
  href="http://www.mozilla.org/products/firefox">Firefox</a> and <a
  href="http://www.mozilla.org/products/thunderbird">Thunderbird</a>),
  image processing, music production (<a
  href="http://www.steinberg.net/en/index.php">Cubase</a>, <a
  href="http://www.cakewalk.com/">Cakewalk</a>, Media players and the
  like), providing my answering machine in a 24/7 environment really
  has been taxing my poor old 450 Mhz machine!

  <p>Next I offloaded most services to my <a
  href="http://www.mandrakesoft.com/">Mandrake 9.1 Linux</a>
  system. Not that the <span class="standout">Linux</span> box is any
  more powered - it's a 333 Mhz machine but doesn't seem that bogged
  down since it's running <span class="standout">Linux</span>. More
  things will move to <span class="standout">Linux</span> because it
  just a lot easier to program and much more stable. Still the <span
  class="standout">Linux</span> box is quite responsive. I'm also
  starting to use <a href="http://mysql.com">MySQL</a> and am playing
  with some weblog software from <a
  href="http://movabletype.org">MovableType</a>.</p>

  <p>I've also set up <a
  href="http://gallery.sourceforge.net/">Gallery</a> which is a <a
  href="http://php.net">PHP</a> web application for managing a <a
  href="/gallery">photo gallery</a> and, of course, requires that your
  web server understands <a href="http://php.net">PHP</a>. I've been
  getting into <a href="http://php.net">PHP</a> lately and have been
  changing <a href="http://defaria.com/maps">MAPS</a> <a
  href="http://perl.com">Perl</a> based web pages with <a
  href="http://php.net">PHP</a> based web pages. For one, since the
  PHP intepreter is embedded in the <a
  href="http://httpd.apache.org">Apache web server</a> it's faster -
  I'd bet that mod_perl would make the <a
  href="http://perl.com">Perl</a> versions as quick but <a
  href="http://php.net">PHP</a> is more "web oriented" than <a
  href="http://perl.com">Perl</a> anyway.</p>

  <p>Finally I've installed <a
  href="http://www.ics.uci.edu/~ejw/authoring/">WebDAV</a> so I can
  host Mozilla's <i>Remote Calendars</i> from my web site. The goal is
  to centralize my data, my life, from my house, under my control and
  be able to provide me with all of my stuff from wherever I am. Also,
  I believe with <a
  href="http://www.ics.uci.edu/~ejw/authoring/">WebDAV</a> I will be
  able to use things like Netscape's <i>publish</i> option so that I
  can even edit my web pages from anywhere.<p>

  <h2><a name="Dell"></a>New Desktop - Dell Dimension 3000</h2>

  <p>After suffering for years with my 1998 450 Mhz PC I finally
  decided to get a new one. I was due! After getting some money from a
  side contract I purchased a Dell Dimension 3000. This sucker's a 3
  Ghz Hyperthreaded CPU with 512 Meg Memory. The video card is a
  little bit better (64 Meg of video memory whereas my old machine had
  only 8 Meg!).</p>

  <p>I got just a DVD player because I figured I could transfer my HP
  9100i CD Writer into this box. I also wanted to transfer the Sound
  Blaster Live Drive which is a CD spaced "drive" that connects to the
  sound card and allows you to plug in many things like michrophones,
  headphone, MIDI keyboards, Optical ins and outs, additional line ins
  and outs. This is convenient to be able to plug right into the front
  of the box. Alas, the Dell only has 2 bays so it was either the Live
  Drive or the CD Writer but not both! I choose the Live Drive at this
  time.</p>

  <p>I also got a voice grade modem hoping that I could finally get a true
  hands free speakerphone working but the damn modem that Dell uses isn't
  even TAPI compliant! I tried to move my USB voice modem from the old
  machine to the new one but was unable to plug it in. So I put this and
  the CD Writer back into the old machine.</p>

  <p>I also wanted to go to 24-bit 192 Khz sampling for a sound card.
  I decided to upgrade to a Sound Blaster Audigy 2 ZS. This card also
  features 5.1/6.1 and 7.1 outputs. I figured I could then play a
  little bit with making my own surround sound recordings. I also get
  a new surround sound set of speakers for this card. I hope to
  eventually meld my PC sound with my Bose Home Theater that I have. I
  now effectively have 2 home theater systems, one on my PC and one in
  my living room. Maybe one day the PC will move to the living room
  and start controlling my entertainment...</p>

  <p>All in all I'm quite happy with its performance. It is much
  faster than the old system and it supports standby. My old system
  supposedly also supported standby but that was on W98. Besides my
  old PC used to be my whole domain so it couldn't standby. Since
  services moved to Linux it could standby but on Windows 2000 and
  later XP it wouldn't standby! Videos play just great, no hesitation,
  no jerkyness and I can do other things while doing heavy
  processing. With the DVD drive came WinDVD so now DVDs work. My old
  system's DVD drive just plain broke.</p>

  <p>Maybe in the future I will move the Linux box up to the 450 Mhz
  (i.e. the old XP box) but right now it's still doing just find</p>

  <h2><a name="Compaq"></a>New server - Compaq Persario 3200+ Althon</h2>

  <p>Well my poor old server has really be stressing lately. With
  Gallery and Andromeda as well as handling email and filtering it
