  that! I'm just gonna be installing Linux on it anyway. Don't need no
  fancy monitors or speaker systems on this machine - hell I don't
  even have a monitor on my servers! I just stick them in a closet and
  access them remotely!</p>

  <p>For example, this system is 2.2Ghz (Athlon 3200+), has 512 Meg of
  memory, a 160 Gig drive. Oh it also has a DVD/CD writer combo and
  even an extra CDROM drive as well as things like connections for
  headphones and USB ports on the front. I won't be using any of those
  but hey they came with the box. I may use the DVD Writer though... I
  believe it had XP Home on it but honestly I never booted it up with
  XP. Just stuck in the Linux boot CD and went from there. Totally
