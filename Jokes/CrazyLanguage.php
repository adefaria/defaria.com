  <h2 class="centered">Our Crazy Language</h2>

  <h3 class="centered">Condensed from "Crazy English" by Richard
  Lederer</h3>

  <h3 class="centered">Reader's Digest, June 1990</h3>
  
  <p><span class="standout">If pro and con are opposites, is Congress
  the opposite of progress?"</span></p>
 
  <p>English is the most widely used language in the history of our
  planet. One in every seven human beings can speak it. More than half
  of the world's books and three-quarters of international mail are in
  English. Of all languages, English has the largest vocabulary -
  perhaps as many as <b>two million</b> words - and one of the noblest
  bodies of literature.</p>
 
  <p>Nonetheless, let's face it: English is a crazy language. There
  is no egg in eggplant, neither pine nor apple in pineapple and no
  ham in a hamburger. English muffins weren't invented in England or
  french fries in France. Sweetmeats are candy, while sweetbreads,
  which aren't sweet, are meat.</p>
 
  <p>We take English for granted. But when we explore its paradoxes,
  we find that quicksand can work slowly, boxing rings are square,
  public bathrooms have no baths and a guinea pig is neither a pig nor
  from Guinea.</p>
 
  <p>And why is it that a writer writes, but fingers don't fing,
  grocers don't groce, humdingers don't hum and hammers don't ham?  If
  the plural of tooth is teeth, shouldn't the plural of booth be
  beeth?  One goose, two geese - so one moose, two meese?  One index,
  two indices - one Kleenex, two Kleenices?</p>
 
  <p>Doesn't it seem loopy that you can make amends but not just one
  amend, that you comb through the annals of history but not just one
  anal?  If you have a bunch of odds and ends and you get rid of all
  but one, what do you call it?</p>
 
  <p>If the teacher taught, why isn't it true that the preacher
  praught?  If a horsehair mat is made from the hair of horses and a
  camel's-hair coat from the hair of camels, from what is a mohair
  coat made?  If a vegetarian eats vegetables, what does a
  humanitarian eat?  If you wrote a letter, perhaps you also bote your
  tongue?</p>
 
  <p>Sometimes I wonder if all English speakers should be committed to
  an asylum for the verbally insane. In what other language do people
  drive on a parkway and park in a driveway?  Recite at a play and
  play at a recital?  Ship by truck and send cargo by ship?  Have
  noses that run and feet that smell?</p>
 
  <p>How can a slim chance and a fat chance be the same, while a wise
  man and a wise guy are opposites?  How can OVERLOOK and OVERSEE be
  opposites, while <b>quite a lot</b> and <b>quite a few</b> are
  alike?  How can the weather be hot as hell one day and cold as hell
  the next?</p>
 
  <p>Did you ever notice that we talk about certain things only when
  they are absent?  Have you ever seen a horseful carriage or a
  strapful gown, met a sung hero or experienced requited love?  Have
  you ever run into someone who was combobulated, gruntled, ruly or
  peccable?</p>
 
  <p>And where are the people who ARE spring chickens or who actually
  <b>would</b> hurt a fly?  I meet individuals who CAN cut the mustard
  and whom I <b>would</b> touch with a ten-foot pole, but I cannot
  talk about them in English.</p>
 
  <p>You have to marvel at the unique lunacy of a language in which
  your house can burn up as it burns down, in which you fill in a form
  by filling it out and in which your alarm clock goes off by going
  on.</p>
 
  <p>English was invented by people, not computers, and it reflects
  the creativity of the human race (which, of course, isn't really a
  race at all). That is why, when stars are out they are visible, but
  when the lights are out they are invisible. Any why, when I wind up
  my watch I start it, but when I wind up this essay I end it.</p>

