  <p>An older lady gets pulled over for speeding...</p>

  <p><b>Older Woman</b>: Is there a problem, Officer?</p>

  <p><b>Officer</b>: Ma'am, you were speeding.</p>

  <p><b>Older Woman</b>: Oh, I see.</p>

  <p><b>Officer</b>: Can I see your license please?</p>

  <p><b>Older Woman</b>: I'd give it to you but I don't have one.</p>

  <p><b>Officer</b>: Don't have one?</p>

  <p><b>Older Woman</b>: Lost it, 4 years ago for drunk driving.</p>

  <p><b>Officer</b>: I see....Can I see your vehicle registration 
  papers please.</p>

  <p><b>Older Woman</b>: I can't do that.</p>

  <p><b>Officer</b>: Why not?</p>

  <p><b>Older Woman</b>: I stole this car.</p>

  <p><b>Officer</b>: Stole it?</p>

  <p><b>Older Woman</b>: Yes, and I killed and hacked up the owner.</p>

  <p><b>Officer</b>: You what?</p>

  <p>Older Woman</b>: His body parts are in plastic bags in the trunk 
  if you want to see.</p>

  <p>The Officer looks at the woman and slowly backs away to his car 
  and calls for back up. Within minutes 5 police cars circle the car. 
  A senior officer slowly approaches the! car, clasping his half drawn 
  gun.</p>

  <p><b>Officer 2</b>: Ma'am, could you step out of your vehicle 
  please! The woman steps out of her vehicle.</p>

  <p><b>Older woman</b>: Is there a problem sir?</p>

  <p><b>Officer 2</b>: One of my officers told me that you have stolen 
  this car and murdered the owner.</p>

  <p><b>Older Woman</b>: Murdered the owner?</p>

  <p><b>Officer 2</b>: Yes, could you open the trunk of your car, 
  please.</p>

  <p>The woman opens the trunk, revealing nothing but an empty trunk.</p>

  <p><b>Officer 2</b>: Is this your car, ma'am?</p>

  <p><b>Older Woman</b>: Yes, here are the registration papers. The 
  officer is quite stunned.</p>

  <p><b>Officer 2</b>: One of my officers claims that you do not have 
  a driving license.</p>

  <p>The woman digs into her handbag and pulls out a clutch purse and 
  hands it to the officer. The officer examines the license. He looks 
  quite puzzled.</p>

  <p><b>Officer 2</b>: Thank you ma'am, one of my officers told me you 
  didn't have a license, that you stole this car, and that you murdered 
  and hacked up the owner.</p>

  <p><b>Older Woman</b>: Bet the liar told you I was speeding, too.</p>


  <p><b>Moral: Don't mess with little old ladies!</p>

