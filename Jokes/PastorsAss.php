  <p>A pastor entered his donkey in a race and it won.</p>

  <p>The pastor was so pleased with the donkey that he entered it in
  the race again, and it won again.</p>

  <p>The local newspaper read:</p>

  <p><b>PASTOR'S ASS OUT FRONT</b></p>

  <p>The Bishop was so upset with this kind of publicity that he
  ordered the pastor not to enter the donkey in another race.</p>

  <p>The next day, the local newspaper headline read:</p>

  <p><b>BISHOP SCRATCHES PASTOR'S ASS</b></p>

  <p>This was too much for the bishop, so he ordered the pastor to get
  rid of the donkey.  The pastor decided to give it to a nun in a
  nearby convent.  The local paper, hearing of the news, posted the
  following headline the next day:</p>

  <p><b>NUN HAS BEST ASS IN TOWN</b></p>

  <p>The bishop fainted. He informed the nun that she would have to
  get rid of the donkey, so she sold it to a farmer for $10.  The next
  day the paper read:</p>

  <p><b>NUN SELLS ASS FOR $10</b></p>

  <p>This was too much for the bishop, so he ordered the nun to buy
  back the donkey and lead it to the plains where it could run wild.
  The next day the headlines read:</p>

  <p><b>NUN ANNOUNCES...HER ASS IS WILD AND FREE</b></p>

  <p>The bishop was buried the next day....</p>

  <p>The moral of the story is: Being concerned about public opinion
  can bring you much grief and misery and even shorten your life. So
  be yourself and enjoy life. Only worry about your own ASS not
  someone else's. You'll be a lot happier and live longer.</p>

