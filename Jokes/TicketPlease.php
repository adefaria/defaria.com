    <p>Two groups of students - math and engineering majors - boarded a
  train that was headed for a technical convention.  Each of the math
  majors had a ticket, but their engineering counterparts had only one
  ticket between them.</p>

  <p>The math majors were snickering at this when an engineering
  student shouted, "Here comes the conductor!"  With that, all the
  engineering majors squeezed into a bathroom.  The puzzled math
  students watched as the conductor collected their tickets, then
  knocked on the bathroom door and said, "Ticket please."  The
  conductor took the single ticket that was passed under the door and
  left.</p>

  <p>Not to be outdone, the math students boarded the returning train
  with only one ticket, and again they laughed because this time their
  rivals had no ticket at all.</p>

  <p>When the engineering lookout yelled, "Conductor coming!" all the
  engineers crowded into one bathroom, while the math majors piled
  into another.  Then, before the conductor entered the car, one of
  the engineers came out of his bathroom and knocked on the math
  majors' door.</p>

  <p>"Ticket please," he said.</p>

