  <p>Three couples -- one elderly, one middle-aged and one newlywed -- wanted
  to join a church.</p>

  <p>The priest said, "We have special requirements for new parishioners. You
  must abstain from having sex for two weeks." The couples all agreed and came
  back at the end of two weeks.</p>

  <p>The pastor went to the elderly couple and asked, "Were you able to abstain
  from sex for the two weeks?"</p>

  <p>The old man replied, "No problem at all, Father."</p>

  <p>"Congratulations! Welcome to the church!" said the priest.</p>

  <p>The priest went to the middle-aged couple and asked, "Well, were you able
  to abstain from sex for the two weeks?"</p>

  <p>The middle-aged man replied, "The first week was not too bad. The second
  week I had to sleep on the couch for a couple of nights, but, yep, we made
  it."</p>

  <p>"Congratulations! Welcome to the church," said the priest.</p>

  <p>The priest then went to the newlywed couple and asked, "Well, were you
  able to abstain from sex for two weeks?"</p>

  <p>"No Pastor, we were not able to go without sex for the two weeks," the
  young man replied sadly.</p>

  <p>"What happened?" inquired the priest.</p>

  <p>"My wife was reaching for a can of corn on the top shelf and dropped it,"
  said the young man. "When she bent over to pick it up, I was overcome with
  lust and took advantage of her right there."</p>

  <p>"You understand, of course, this means you will not be welcome in our
  church," stated the priest.</p>

  <p>"We know," said the young man. "We're not welcome at the supermarket
  anymore either."</p>

